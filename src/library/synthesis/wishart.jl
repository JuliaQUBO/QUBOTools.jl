@doc raw"""
    Wishart{T}(n::Integer, m::Integer)

Represents the Wishart model on ``n`` variables whose ``W`` matrix
has ``m`` columns.
"""
struct Wishart{T} <: AbstractProblem{T}
    n::Int
    m::Int

    function Wishart{T}(n::Integer, m::Integer) where {T}
        return new{T}(n, m)
    end
end

Wishart(n::Integer, m::Integer) = Wishart{Float64}(n, m)

function generate(rng, problem::Wishart{T}) where {T}
    f = PBO.wishart(
        rng,
        PBO.PBF{Int,T},
        problem.n,
        problem.m,
    )

    return Model{Int,T,Int}(
        f;
        metadata = Dict{String,Any}(
            "origin"    => "Generated by QUBOTools.jl",
            "synthesis" => Dict{String,Any}(
                "model"      => "Wishart",
                "parameters" => Dict{String,Any}(
                    "n" => problem.n,
                    "m" => problem.m,
                ),
            ),
        )
    )
end

